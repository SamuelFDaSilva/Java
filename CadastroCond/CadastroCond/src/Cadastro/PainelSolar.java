package Cadastro;

public class PainelSolar {
		
		private int idPlaca;
		private double potencia;

		public PainelSolar(int idPlaca, double potencia) {
			this.idPlaca = idPlaca;
			this.potencia = potencia;
		}
		
		public int getidPlaca() {
			return idPlaca;
		}
		
		public double getPotencia() {
			return potencia;
		}
		

		
	}

